    const translations = AUTH_TRANSLATIONS[locale];
    const block = formatTranslationBlock(locale, translations);

    // Find the locale section and insert at the end (before closing brace)
    const localePattern = new RegExp(`(\\s+${locale}: \\{[\\s\\S]*?)(\\n\\s+\\},)`, 'g');
