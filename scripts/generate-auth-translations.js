function formatTranslationBlock(locale, keys) {
  const indent = '    ';
  let block = '';

  for (const [key, value] of Object.entries(keys)) {
    // Escape single quotes in the value
    const escapedValue = value.replace(/'/g, "\\'");
    block += `${indent}'${key}': '${escapedValue}',\n`;
  }

  return block.trim();
}

